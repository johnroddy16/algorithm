# in this list of numbers it would take us 3 steps to find the number 1 with a binary search and there are 8 numbers,
# 2 ** 3 happens to be 8
# we can write log 2 8 = 3, normally the 2 would be a small 2 subscripted
# log 2 8 is asking 2 to what power equals 8 and in this case log 2 8 = 3

# you can also say it asks how many times do you have to divide 8 by 2 to get down to one item, and of course it is 3 times

# O(log n) is very flat on a graph and very time efficient

